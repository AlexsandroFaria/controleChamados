    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir este chamado?", "Exclusão de chamado", JOptionPane.YES_NO_OPTION);
        if (opcao == 0) {
            int numeroChamado = Integer.parseInt(txtNumeroChamado.getText());
            chamadoDao.excluirChamado(numeroChamado);
        }
        limparCampos();
        atualizarTabela();
        btExcluir.setEnabled(false);
        btAlterar.setEnabled(false);
        btFecharChamado.setEnabled(false);
        btCadastrar.setEnabled(true);
        jtChamados.setSelectedIndex(1);
    }//GEN-LAST:event_btExcluirActionPerformed

