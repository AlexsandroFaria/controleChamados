    private void btBuscarContratoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarContratoActionPerformed
        // TODO add your handling code here:
        String contrato = txtContrato.getText();

        try {
            if (txtContrato.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo contrato não pode estar em branco!", "AVISO", JOptionPane.WARNING_MESSAGE);
            } else {
                ChamadoDao chamadoDao = new ChamadoDao();
                List<Cliente> listaClientes = chamadoDao.listarClientesPorContrato(contrato);

                for (Cliente cliente : listaClientes) {
                    txtNomeCliente.setText(cliente.getNomeCliente());
                    txtEndereco.setText(cliente.getEndereco());
                    txtContato.setText(cliente.getContato());
                    txtTelefone.setText(cliente.getTelefone());
                    txtEmail.setText(cliente.getEmail());
                }
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro na consulta");
        }

    }//GEN-LAST:event_btBuscarContratoActionPerformed

    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        String numeroChamado = txtNumeroChamado.getText();
        try {
            if (txtNumeroChamado.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo NÚMERO DO CHAMADO não pode estar em branco!", "AVISO", JOptionPane.WARNING_MESSAGE);
            } else if (txtContrato.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo CONTRATO não pode estar em branco!", "AVISO", JOptionPane.WARNING_MESSAGE);
            } else if (txtProblema.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo PROBLEMA não pode estar em branco!", "AVISO", JOptionPane.WARNING_MESSAGE);
            } else if (comboStatus.getSelectedItem().equals("Selecione uma opção")) {
                JOptionPane.showMessageDialog(null, "Em STATUS selecione uma opção Válida!", "AVISO", JOptionPane.WARNING_MESSAGE);
            } else {
                Chamado chamado = new Chamado();
                if (txtNumeroChamado.getText().equals(chamado.getNumeroChamado())) {
                    JOptionPane.showMessageDialog(null, "Número do chamado já existe");
                } else {
                    chamado.setNumeroChamado(Integer.parseInt(txtNumeroChamado.getText()));
                    chamado.setContrato(Integer.parseInt(txtContrato.getText()));
                    chamado.setNomeCliente(txtNomeCliente.getText());
                    chamado.setEndereco(txtEndereco.getText());
                    chamado.setContato(txtContato.getText());
                    chamado.setTelefone(txtTelefone.getText());
                    chamado.setEmail(txtEmail.getText());
                    chamado.setProblema(txtProblema.getText());
                    chamado.setObservacao(txtObservação.getText());
                    chamado.setStatus((String) (comboStatus.getSelectedItem()));
                    chamado.setData(txtData.getText());
                    chamadoDao.cadastrarChamado(numeroChamado, chamado);
                    limparCampos();
                    atualizarTabela();
                    btFecharChamado.setEnabled(false);
                    btAlterar.setEnabled(false);
                    btExcluir.setEnabled(false);
                    txtContrato.setEnabled(true);
                    jtChamados.setSelectedIndex(1);
                }
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro no cadastro de Chamado", "ERRO", JOptionPane.WARNING_MESSAGE);
            throw new RuntimeException(erro);
        }

    }//GEN-LAST:event_btCadastrarActionPerformed

