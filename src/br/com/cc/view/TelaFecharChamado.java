    private void btConsultarChamadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultarChamadoActionPerformed
        // TODO add your handling code here:
        String numeroChamado = txtNumeroChamado.getText();
        try {
            if (txtNumeroChamado.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo NUMERO DO CHAMADO não pode estar em branco");
            }else{
                ChamadoFechadoDao chamadoFechadoDao = new ChamadoFechadoDao();
                List<Chamado>listarChamado = chamadoFechadoDao.listarChamadosFechadosPorNumeroChamado(numeroChamado);
                for (Chamado chamado : listarChamado){
                    txtContrato.setText(Integer.toString(chamado.getContrato()));
                    txtNomeCliente.setText(chamado.getNomeCliente());
                }
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao efetuar a consulta");
            throw new RuntimeException(erro);
        }
    }//GEN-LAST:event_btConsultarChamadoActionPerformed

