    private void btFecharChamadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFecharChamadoActionPerformed
        // TODO add your handling code here:
        int numeroChamadoFechado = Integer.parseInt(txtNumeroChamado.getText());
        try {
            if (comboStatus.getSelectedItem().equals("Selecione uma opção")) {
                JOptionPane.showMessageDialog(null, "Informe um valor válido no campo STATUS.");
            } else if (txtData.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Informe uma DATA válida para fechamento.");
            } else {
                ChamadosFechados chamadosFechados = new ChamadosFechados();
                chamadosFechados.setNumeroChamado(Integer.parseInt(txtNumeroChamado.getText()));
                chamadosFechados.setContrato(Integer.parseInt(txtContrato.getText()));
                chamadosFechados.setNomeCliente(txtNomeCliente.getText());
                chamadosFechados.setProblema(txtProblema.getText());
                chamadosFechados.setMensagemFechamento(txtMensEncerramento.getText());
                chamadosFechados.setStatus((String) comboStatus.getSelectedItem());
                chamadosFechados.setDataFechamento(txtData.getText());
                ChamadoFechadoDao chamadoFechadoDao = new ChamadoFechadoDao();
                chamadoFechadoDao.cadastrarChamadoFechado(chamadosFechados);

                ChamadoDao chamadoDao = new ChamadoDao();
                chamadoDao.excluirChamadoFechado(numeroChamadoFechado);
                txtNumeroChamado.setEnabled(true);
                txtContrato.setEnabled(true);
                txtNomeCliente.setEnabled(true);
                limparTela();
                atualizarTabela();
            }
        } catch (Exception erro) {
            throw new RuntimeException(erro);
        }
    }//GEN-LAST:event_btFecharChamadoActionPerformed

